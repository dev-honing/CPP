#include <iostream>
#include <string>

int main(void) {
	// 문제 10. 변수 a 값의 변화 - 변수 a를 선언하고 8을 할당한 뒤, `a++`의 개수를 조절해 a의 값을 12로 출력되도록 식을 작성하시오.(조사식으로 확인하며 작성)

	// 10-1. 변수 a 선언
	int a; // 455(쓰레기 값)

	// 10-2. 값으로 8을 할당
	a = 8; // 8

	// 10-3. 식 작성(조사식)
	a++; // 9
	a++; // 10
	a++; // 11
	a++; // 12
	


	return 0;
}