#include <iostream>
#include <string>

int main(void) {

    // 문자형과 문자열
    char i = 'I'; // 문자형 변수 선언 및 초기화
    std::string myName = "BHN"; // 문자열 변수 선언 및 초기화

    // 콘솔 출력
    std::cout << "변수 i에 할당된 값: " << i << std::endl;
    std::cout << "변수 myName에 할당된 값: " << myName << std::endl;

    return 0;
}
